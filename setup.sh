# –°–æ–∑–¥–∞–µ–º —Ñ–∞–π–ª .envrc –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π –∞–∫—Ç–∏–≤–∞—Ü–∏–∏
